// C++ Program to Find Quotient and Remainder
#include <iostream>
using namespace std;

int main(){
    int x, y;
    cout << "..." << endl;
    cout << "Divider: ";
    cin >> x;
    cout << "Divisor: ";
    cin >> y;
    cout << "..." << endl;
    cout << "Quotient: " << x / y << endl;
    cout << "Reminder: " << x % y << endl;
    cout << "..." << endl;
}
