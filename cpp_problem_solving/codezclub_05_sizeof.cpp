// C++ Program to Find Size of int, float, double and char
#include <iostream>
using namespace std;

int main(){
    int m = 1;
    char n = n;
    double o = 1.65;
    string p = "Hello";

    cout << "..." << endl;
    cout << "Size of int is     : " << sizeof(m) << endl;
    cout << "Size of char is    : " << sizeof(n) << endl;
    cout << "Size of double is  : " << sizeof(o) << endl;
    cout << "Size of string is  : " << sizeof(p) << endl;
    cout << "..." << endl;
}